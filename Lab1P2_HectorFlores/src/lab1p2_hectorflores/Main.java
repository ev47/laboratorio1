    private void sellTicketActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_sellTicketActionPerformed
        String name = JOptionPane.showInputDialog("Nombre del pasajero: ");
        name = name.toLowerCase();
        metodos.sellTicket(name);
        name = capitalizarPrimeraLetra(name);
        (metodos.asientos[metodos.firstAvailable() - 1]).setNombre(name);
    }//GEN-LAST:event_sellTicketActionPerformed

    private void cancelTicketActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cancelTicketActionPerformed
        String name = JOptionPane.showInputDialog("Nombre del pasajero que desea cancelar su ticket: ");
        if(metodos.cancelTicket(name)){
            JOptionPane.showMessageDialog(null, "Se ha cancelado el ticket de " + name + " exitosamente.");
        } else {
            JOptionPane.showMessageDialog(null, "El pasajero " + name + " no esta registrado en el avion.", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_cancelTicketActionPerformed

